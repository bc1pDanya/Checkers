#pragma once

enum class Response
{
    OK,      // Успешное завершение операции — всё прошло нормально и можно продолжать игру.
    BACK,    // Запрос на откат/возврат хода (пользователь хочет "назад" — отменить предыдущий ход).
    REPLAY,  // Запрос на повтор партии — пользователь/интерфейс хочет начать игру заново.
    QUIT,    // Запрос на завершение игры — пользователь хочет выйти/прервать партию.
    CELL     // Выбор клетки: сигнализирует, что вместе с этим кодом возвращаются координаты выбранной клетки
             // (например, возвращается кортеж вида (Response::CELL, x, y) из hand.get_cell()).
};
